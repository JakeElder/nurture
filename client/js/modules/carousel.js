//==============================================================================
// Constructor
//==============================================================================

var Carousel = function(el) {
  'use strict';
  this.$el      = $(el);
  this.$entries = this.$el.find('.carousel__entry');
};
var proto = Carousel.prototype;


//==============================================================================
// Public functions
//==============================================================================

proto.show = function(entry) {
  'use strict';
  this.$entries.removeClass('active');
  $(entry).addClass('active');
};


//==============================================================================
// Export
//==============================================================================

module.exports = Carousel;

