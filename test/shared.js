//==============================================================================
// Dependencies
//==============================================================================

var request = require('supertest');
var should  = require('should');
var app     = require('../app');


//==============================================================================
// Setup
//==============================================================================

var shared = {};


//==============================================================================
// Shared tests
//==============================================================================

shared.shouldReturnOK = function() {

  'use strict';

  it('should return 200 OK', function(done) {
    request(app)
      .get(this.url)
      .expect(200)
      .end(done);
  });
};


//==============================================================================
// Export
//==============================================================================

module.exports = shared;

